package org.example;

public class ConvertIntTypeVariablesToString3 {

    public static void main(String[] args) {

        int num1 = 3476;
        int num2 = 8656;

        String str1 = "" + num1;
        String str2 = "" + num2;

        System.out.println(str1);    // 3476
        System.out.println(str2);    // 8656
    }

}
