package org.example;

public class ConvertLongTypeVariablesIntoInt2 {

    public static void main(String[] args) {

        long a = 2322331L;
        long b = 52341241L;

        int c = (int)a;
        int d = (int)b;

        System.out.println(c);    // 2322331
        System.out.println(d);    // 52341241
    }
}
