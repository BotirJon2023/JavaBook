package org.example;

public class ConvertCharTypeVariablesToInt {

    public static void main(String[] args) {

        char a = '5';
        char b = 'c';

        int num1 = a;
        int num2 = b;

        System.out.println(num1);    // 53
        System.out.println(num2);    // 99
    }
}
