package org.example;

public class ConvertIntTypeVariablesToChar {

    public static void main(String[] args) {

        int num1 = 80;
        int num2 = 81;

        char a = (char) num1;
        char b = (char) num2;

        System.out.println(a);    // P
        System.out.println(b);    // Q
    }

}
