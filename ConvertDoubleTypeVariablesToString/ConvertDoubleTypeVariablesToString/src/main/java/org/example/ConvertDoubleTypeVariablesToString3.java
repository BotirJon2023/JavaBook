package org.example;

public class ConvertDoubleTypeVariablesToString3 {

    public static void main(String[] args) {

        double num1 = 347.6D;
        double num2 = 86.56D;

        String str1 = "" + num1;
        String str2 = "" + num2;

        System.out.println(str1);    // 347.6
        System.out.println(str2);    // 86.56
    }
}
